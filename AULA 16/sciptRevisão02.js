let a = 50
let b = 32
let soma = a + b

let resposta = Number(prompt(`Qual a soma de ${a} + ${b}`))
let acertos = 0

if(resposta == soma){
acertos += 1
alert(`Parabens, acertou! a soma ${a} + ${b}`)
}

else{
    alert(`Que pena, errou! A soma é ${a} + ${b} = ${soma}`)
}


